package com.ulcdia.experiment_5.math_examples;

public class MathExamples {
    public static void main(String[] args) {
        System.out.printf("%20s = %.2f\n", "pow(10.24, 2.048)", Math.pow(10.24, 2.048));
        System.out.printf("%20s = %.2f\n", "log(1024)", Math.log(1024));
        System.out.printf("%20s = %.2f\n", "sqrt(2048)", Math.sqrt(2048));
        System.out.printf("%20s = %.2f\n", "tan(64)", Math.tan(64));
        System.out.printf("%20s = %.2f\n", "random()", Math.random());
    }
}
